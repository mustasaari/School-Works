package coordinate;

public class Point {
        private int x;  //has private access in Point
        private int y;

        public int getY() {
            return y;
        }
        public int getX() {
            return x;
        }
        public void setX(int par) {
            x = par;
        }
        public void setY(int par) {
            y = par;
        }
}